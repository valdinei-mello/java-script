/*
Operadores de Comparação

> maior que
< menor que
>= maior e igual
<= menor e igual
== igualdade
=== igualdade estrita (valor e tipo)
!= diferente
!== diferente estrita (valor e tipo)
*/

console.log(54>14); // 54 é maior que 14? true or false
console.log(10 >= 10); // 10 é maior ou igual a 10 ? True or False
console.log(10< 18); // 10 é menor que 11? 
console.log(10<= 18); // 10 é menor e ou igual a 18?
console.log(10 == 10); // 10 é igual a 10 ?

const n1 = '10'; // string
const n2 = 10; // number
console.log(n1 === n2); // verifica se o valor e tipo da variavel são identicos

const x1 = 10;
const x2 = 101;
console.log(x1 != x2); //comparara apenas o valor !// x1 é diferente de x2?

const z1 = 10;
const z2 = '10';
console.log(z1 !== z2); //compara valor e tipo // o valor de z1 e seu tipo são diferenres de z2 ?

 